        public  string Code_Encrypt(string code)
        {
            try
            {
                code = code.Replace("+", "_XXXZZZ_");
                code = code.Replace("/", "^XXXYYY^");
                code = code.Replace("=", "!NMMN!");
            }
            catch (Exception ex)
            {
                ErrorExceptionLogingByService(ex.ToString(), "CryptorEngine" + ":" + new StackTrace().GetFrame(0).GetMethod().Name, "CryptorEngine", "NA", "NA", code,"WEB");
            }
            return code;
        }
        public  string Code_Decrypt(string code)
        {
            try
            {
                code = code.Replace("_XXXZZZ_", "+");
                code = code.Replace("^XXXYYY^", "/");
                code = code.Replace("!NMMN!", "=");
            }
            catch (Exception ex)
            {
                //CPPLException objError = new CPPLException();
                ErrorExceptionLogingByService(ex.ToString(), "CryptorEngine" + ":" + new StackTrace().GetFrame(0).GetMethod().Name, "CryptorEngine", "NA", "NA", code,"WEB");
            }
            return code;
        }


